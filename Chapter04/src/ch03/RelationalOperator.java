package ch03;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int num1 = 30;
		int num2 = 2;
		
		System.out.println( num1 == num2 );
		System.out.println( num1 > num2 );
		System.out.println( num1 < num2 );
		System.out.println( num1 >= num2 );
		System.out.println( num1 <= num2 );
		System.out.println( num1 != num2 );

	}

}
