package ch03;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 2;
		float num3 = 2.0f;
		int num4 = 4;
		
		System.out.println( num1 + num2 );
		System.out.println( num1 - num2 );
		System.out.println( num1 * num2 );
		System.out.println( num1 / num2 );
		System.out.println( num1 / num3 );
		System.out.println( num1 % num3 );
		System.out.println( num1 % num4 );

	}

}
